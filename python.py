# Напишите программу, которая выводит максимальный элемент в заштрихованной области квадратной матрицы (нижний левый угол).

n = int(input())
s = [list(map(int, input().split())) for _ in range(n)]
mx = -1000
for i in range(n):
    for j in range(n):
        if i >= j:
            mx = max(mx, s[i][j])
print(mx)

